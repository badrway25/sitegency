# Translation engine
# ==============================================================
MAX_LEN = 180  # chars — increased to catch longer heading phrases


def translate_text(text, lookup):
    """Translate a text node if it matches a known phrase.

    Handles:
    - Exact match
    - Trailing punctuation (!, ., :, ?)
    - Leading/trailing whitespace preserved
    - Title-case matching
    """
    if not text or not lookup:
        return text
    stripped = text.strip()
    if not stripped or len(stripped) > MAX_LEN:
        return text
    if not any(c.isalpha() for c in stripped):
        return text

    # Try direct lookup
    key = stripped.lower()
    if key in lookup:
        prefix = text[:len(text) - len(text.lstrip())]
        suffix = text[len(text.rstrip()):]
        return prefix + lookup[key] + suffix

    # Try stripping trailing punctuation
    import re as _re
    m = _re.match(r'^(.*?)([!\.\?:;,\s]+)$', stripped)
    if m:
        core = m.group(1).strip()
        tail = m.group(2)
        if core.lower() in lookup:
            prefix = text[:len(text) - len(text.lstrip())]
            suffix = text[len(text.rstrip()):]
            return prefix + lookup[core.lower()] + tail + suffix

    # Try stripping leading punctuation/symbols (e.g. "- About Us" → "About Us")
    m2 = _re.match(r'^([^\w]*)(.*)', stripped)
    if m2 and m2.group(1):
        lead = m2.group(1)
        core = m2.group(2).strip()
        if core.lower() in lookup:
            prefix = text[:len(text) - len(text.lstrip())]
            suffix = text[len(text.rstrip()):]
            return prefix + lead + lookup[core.lower()] + suffix
        # Try both leading AND trailing
        m3 = _re.match(r'^(.*?)([!\.\?:;,\s]+)$', core)
        if m3:
            inner = m3.group(1).strip()
            tail = m3.group(2)
            if inner.lower() in lookup:
                prefix = text[:len(text) - len(text.lstrip())]
                suffix = text[len(text.rstrip()):]
                return prefix + lead + lookup[inner.lower()] + tail + suffix

    return text
