def pool_for(category_name: str, locale: str) -> dict:
    """Return the content pool for a given marketplace category + locale.

    Falls back to the 'default' pool if the category isn't mapped, and to 'en'
    if the locale isn't available.
    """
    key = CATEGORY_TO_POOL.get(category_name, 'default')
    cat_pool = CONTENT_POOL.get(key, CONTENT_POOL['default'])
    return cat_pool.get(locale, cat_pool.get('en', {}))


def images_for(category_name: str) -> list:
    """Return a list of Unsplash image URLs for the given category."""
    key = CATEGORY_TO_POOL.get(category_name, 'default')
    return INDUSTRY_IMAGES.get(key, INDUSTRY_IMAGES['default'])
